        // dd($request->all()); 
        // 创建评论模型
        $comment = new Comment;
        

        $comment -> username = $request->username;
        $comment -> image = $request->image;
        $comment -> content = $request->content;
        $comment -> good_id = $request->good_id;

        if($comment -> save()){
            return back()->with('success', '添加成功');
        }else{
            return back()->with('error','添加失败');
        }
    }

